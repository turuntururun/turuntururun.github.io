const available = (releaseDate: string) => {
  const date = new Date(Date.parse(releaseDate))
  date.setFullYear(date.getFullYear() + 10)
  while (date.getDay() !== 1) {
    date.setDate(date.getDate() + 1)
  }
  return new Date() < date
}

